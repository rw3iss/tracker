/**
 * Category of a breadcrumb trail entry.
 *
 * @see {@link Breadcrumb}
 */
export type BreadcrumbCategory = 'navigation' | 'click' | 'console' | 'network' | 'custom';

/**
 * Severity level of a breadcrumb entry.
 *
 * @see {@link Breadcrumb}
 */
export type BreadcrumbLevel    = 'debug' | 'info' | 'warning' | 'error';

/**
 * A breadcrumb records a single action or event leading up to an error.
 *
 * Breadcrumbs are collected by the `BreadcrumbsPlugin` and attached to
 * error events, providing a timeline of what happened before the error occurred.
 *
 * @see {@link BreadcrumbCategory}
 * @see {@link BreadcrumbLevel}
 */
export interface Breadcrumb {
  /** Unix ms when the breadcrumb was recorded. */
  timestamp: number;
  /** What kind of action this breadcrumb represents. */
  category:  BreadcrumbCategory;
  /** Human-readable description of the breadcrumb. */
  message:   string;
  /** Severity level of this breadcrumb entry. */
  level?:    BreadcrumbLevel;
  /** Additional structured data associated with this breadcrumb. */
  data?:     Record<string, unknown>;
}
